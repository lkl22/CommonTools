# 定义一个ColorEnum枚举类定义Color枚举常量
from enum import Enum


class ColorEnum(Enum):
    Black = '#000000'
    White = '#FFFFFF'
    Red = '#FF0000'
    Green = '#00FF00'
    Blue = '#0000FF'
    # 橙色
    Orange = '#FFA500'
    # 紫色
    Purple = '#A020F0'
    # 青色
    Teal = '#00FFFF'
    # 黄色
    Yellow = '#FFFF00'
    # 深红色
    DarkRed = '#FF00FF'
